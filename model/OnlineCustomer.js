import mongoose from "mongoose"

const onlineCustomerSchema = new mongoose.Schema(
  {
    userName: {
      type: String,
      required: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const OnlineCustomer = mongoose.model("OnlineCustomer", onlineCustomerSchema);
